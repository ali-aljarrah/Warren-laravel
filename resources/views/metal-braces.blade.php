                    <div class="bg-child-blue rounded-4 mt-4 mb-4">
                      <div class="p-4">
                         <h5 class="soft-dark py-4 fs-24 fw-bold">
                             What Are Traditional Braces?
                          </h5>
                         <p class="soft-dark fs-16">
                             Metal braces use a small square metal bracket that is attached to the front of each tooth with special bonding cement. Some teeth, such as your back molars, may need a metal band that wraps around the whole tooth instead of a bracket. Wires are then placed onto the brackets, tightened & kept in place with colored rubber bands called ligatures. You may also need separate removable rubber bands that move your jaw into place. At each appointment, the orthodontist will check your progress, adjust the wires & replace your ligatures.
                         </p>
                      </div>
                    </div>
                    <h5 class="black-color py-4 fs-24 fw-bold">
                        Procedure Overview
                    </h5>
                    <p class="black-color fs-16">
                         If you are not sure braces are worth the cost, consider this: Orthodontic treatment serves more purposes than simply creating a better-looking smile. Teeth that are overcrowded & misaligned can cause more severe dental issues in the future. For example, upper & lower teeth that don’t fit together can grind against each other & eventually lead to damage or even tooth loss. Furthermore, crooked & crowded teeth may be harder to keep clean due to areas that are difficult or impossible to access with a toothbrush or dental floss. Because of this, braces are an important tool for enabling oral hygiene & may be necessary to prevent tooth decay & gum disease.
                         </p>
                        <p class="black-color fs-16">
                         On the other hand, wearing metal braces can also harm your dental hygiene if you do not maintain a rigorous hygiene routine. Food & plaque can easily become stuck in the brackets, so brushing your teeth after every meal & flossing at least once a day is highly advisable. Yes, you still need to floss your teeth even though you have a metal wire in the way! Your orthodontist can give you some tips, & there are appliances that help you “thread” the floss around the wire. You also need to come to your regularly scheduled dental cleaning appointments, of course.
                         </p>
                        <p class="black-color fs-16">
                         If you want to prevent gunk from getting in your braces in the first place, you can start by following a few dietary restrictions. Avoid foods that are chewy, sticky, crunchy or hard. Caramel, chewing gum & popcorn are a few of braces’ worst enemies. They can get stuck in your brackets, loosen a wire or even damage your braces. As usual, sugary foods & drinks that cause cavities should be kept to a minimum.
                         </p>
                        <p class="black-color fs-16">
                         Failure to keep your teeth clean while wearing braces can lead to stained teeth, tooth decay & gum disease. Despite being traps for plaque & food particles, braces do have one hygiene benefit—they will force you to overcome any poor brushing & flossing habits! Keep in mind that the difficulty of cleaning teeth with metal braces is a small price to pay for a healthier & more attractive smile.
                         </p>
