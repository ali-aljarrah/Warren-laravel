                        Procedure Overview
                    </h3>
                    <p class="black-color fs-16">
                        When going in for an air abrasion procedure, patients should still expect some preparation before the tool is even turned on. Air abrasion removes decay by turning it into a powder, which needs to be removed safely. Dentists use suction to catch these particles, but also take measures to protect patients in case the particles escape the procedure area. For example, patients should expect their nose & eyes to be covered during the procedure to minimize the chance of rogue particles causing discomfort or damage there.
                    </p>
