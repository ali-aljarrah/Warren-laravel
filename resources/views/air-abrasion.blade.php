                            What is Air Abrasion?
                        </h2>
                        <p class="soft-dark fs-16">
                            Air abrasion is a drill-less alternative for removing decay that uses a specialized dental tool to spray compressed air & tiny abrasive particles in a fine stream. Air abrasion gently & gradually removes spots of tooth decay or staining from the surface of teeth—without harming the natural tooth structure. The removed decay & particles are then suctioned away. What’s left is a textured surface well-suited to holding onto restorative materials to “build” the tooth back to its natural shape.
                        </p>
                        <p class="soft-dark fs-16 mb-0">
                            Additionally, because air abrasion is gentler on patients’ healthy teeth structures & mouth, they don’t need to rely on anesthesia to remain comfortable during a procedure with air abrasion. That means that in addition to not needing drills, we don’t need to use needles! Plus, because anesthesia isn’t necessary with air abrasion, patients don’t need to wait hours for the numbing effect to dissipate so they can continue with their lives.
